    public boolean isEmptyStudent() {
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra el nombre del estudiante");
            return false;
        }

        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "No se encuentra la fecha de nacimiento del estudiante");
            return false;
        }

        if (jDateChooser1.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(this, "Los estudiantes del futuro no son admitidos");
            return false;
        }
        if (jTextField3.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra esta dirección de correo del estudiante");
            return false;
        }
        if (!jTextField3.getText().matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Dirección de correo inválida");
            return false;
        }
        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra el número celular del estudiante");
            return false;
        }

        if (jTextField4.getText().length() >= 9) {
            JOptionPane.showMessageDialog(this, "El número de dígitos excede la longitud establecida en Honduras");
            return false;
        }

        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra el nombre del tutor del estudiante");
            return false;
        }

        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra el nombre de la tutora del estudiante");
            return false;
        }

        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra la dirección nro. 1 del estudiante");
            return false;
        }

        if (jTextField8.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encuentra la dirección nro. 2 del estudiante");
            return false;
        }
        if (imagePath == null) {
            JOptionPane.showMessageDialog(this, "La imagen del estudiante es obligatoria");
            return false;
        }
        return true;

    }

